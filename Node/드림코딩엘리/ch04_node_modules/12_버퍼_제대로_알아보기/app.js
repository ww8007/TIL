// C++ Go 버퍼 개념 익숙
const fs = require('fs');
const buf = Buffer.from('Hi');
console.log(buf);
console.log(buf.length);
console.log(buf[0]); // 배열 접근시 -> 아스키 코드
console.log(buf[1]);
console.log(buf.toString()); // 기본값 utf-8

//create
const buf2 = Buffer.alloc(2); // 사용 가능한 덩이리 찾아서 초기화
const buf3 = Buffer.allocUnsafe(2); // 초기화 안되어서 빠르지만 데이터가 들어있을 수 있음
buf2[0] = 72;
buf2[1] = 105;
console.log(buf2.toString());
console.log(buf3);

// copy
buf2.copy(buf3);
console.log(buf3.toString());

// concat
const newBuf = Buffer.concat([buf, buf2, buf3]);
console.log(newBuf.toString());
