function hello() {
	console.log(this);
	console.log(this === global);
}

class A {
	constructor(num) {
		this.num = num;
	}
	memberFunction() {
		console.log('------Class------');
		console.log(this);
		console.log(this === global);
	}
}
hello();
const a = new A(1);
a.memberFunction();
console.log('------global scope------');
console.log(this);
console.log(this === module.exports);
