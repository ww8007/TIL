const fs = require('fs');

try {
	fs.renameSync('./text.txt', './file_new.txt');
} catch (e) {
	console.error(e);
}

console.log('hello');

fs.rename('./file_new.txt', './text.txt', (e) => {
	console.error(e);
});

console.log('hello');

fs.promises
	.rename('./text.txt', './text_new/txt')
	.then(() => console.log('Done!'))
	.catch((e) => console.error(e));
